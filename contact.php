  <script>
  $(document).ready(function () {
    $("#btnSubmit").click(function (event) {
      event.preventDefault(); // Prevent form submission

      var data = new FormData($('#contactform')[0]); // Creating a FormData object

      // Clear any previous error messages
      $(".error").html('');

      // Validation checks
      if ($('#fname').val().trim() == '') {
        $("#fnameErr").html("Please provide First Name");
        $('#fname').focus();
        return;
      }

      if ($('#lname').val().trim() == '') {
        $("#lnameErr").html("Please provide Last Name");
        $('#lname').focus();
        return;
      }

      if ($('#email').val().trim() == '') {
        $("#emailErr").html("Please provide Email");
        $('#email').focus();
        return;
      }

      if ($('#uphone').val().trim() == '') {
        $("#phoneErr").html("Please provide Phone Number");
        $('#uphone').focus();
        return;
      }

      if ($('#msg').val().trim() == '') {
        $("#msgErr").html("Please provide Message");
        $('#msg').focus();
        return;
      }

      // Disable submit button to prevent multiple submissions
      $("#btnSubmit").prop("disabled", true);

      // AJAX request to send the form data
      $.ajax({
        type: "POST",
        url: "send_email.php", // Pass action here
        data: data,
        processData: false,
        contentType: false,
        cache: false,
        success: function(response) {
          console.log(response);

          // On success, show a success message and clear the form
          $("#mail-status").removeClass('text-danger').addClass('text-success').html("We have received your enquiry successfully!");
          $("#btnSubmit").prop("disabled", false);

          // Clear the form fields
          $('#contactform')[0].reset();
        },
        error: function(error) {
          // On error, show an error message
          $("#mail-status").removeClass('text-success').addClass('text-danger').html("Something went wrong..Please try again!");
        }
      });
    });
  });
</script>


